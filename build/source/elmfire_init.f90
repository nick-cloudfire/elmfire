! *****************************************************************************
SUBROUTINE ROTATE_ASP_AND_WD (ITYPE)
! *****************************************************************************

INTEGER, INTENT(IN) :: ITYPE
INTEGER :: IBAND, IROW, ICOL

SELECT CASE (ITYPE)
   CASE (1) ! Aspect
      DO IROW = 1, ASP%NROWS
      DO ICOL = 1, ASP%NCOLS
         ASP%R4(ICOL,IROW,1) = ASP%R4(ICOL,IROW,1) - GRID_DECLINATION
         IF (ASP%R4(ICOL,IROW,1) .GT. 360.) ASP%R4(ICOL,IROW,IBAND) = ASP%R4(ICOL,IROW,IBAND) - 360.
         IF (ASP%R4(ICOL,IROW,1) .LT.   0.) ASP%R4(ICOL,IROW,IBAND) = ASP%R4(ICOL,IROW,IBAND) + 360.
         CONTINUE
      ENDDO
      ENDDO
   CASE (2) ! Wind direction
      DO IBAND = 1, WD%NBANDS
      DO IROW = 1, WD%NROWS
      DO ICOL = 1, WD%NCOLS
         WD%R4(ICOL,IROW,IBAND) = WD%R4(ICOL,IROW,IBAND) - GRID_DECLINATION
         IF (WD%R4(ICOL,IROW,IBAND) .GT. 360.) WD%R4(ICOL,IROW,IBAND) = WD%R4(ICOL,IROW,IBAND) - 360.
         IF (WD%R4(ICOL,IROW,IBAND) .LT.   0.) WD%R4(ICOL,IROW,IBAND) = WD%R4(ICOL,IROW,IBAND) + 360.
         CONTINUE
      ENDDO
      ENDDO
      ENDDO
END SELECT

! *****************************************************************************
END SUBROUTINE ROTATE_ASP_AND_WD
